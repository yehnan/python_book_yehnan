


def f(n):
    m1 = 0b10101010
    mask1 = (m1 << 24) | (m1 << 16) | (m1 << 8) | m1
    m2 = 0b01010101
    mask2 = (m2 << 24) | (m2 << 16) | (m2 << 8) | m2
    
    return ((n & mask1) >> 1) | ((n & mask2) << 1)


